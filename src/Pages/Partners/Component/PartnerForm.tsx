class MultiStepForm extends Component {
  state = {
    step: 1,
    firstName: "",
    lastName: "",
    email: "",
    address: "",
    city: "",
    state: "",
    zip: "",
  };

  nextStep = () => {
    const { step } = this.state;
    this.setState({
      step: step + 1,
    });
  };

  prevStep = () => {
    const { step } = this.state;
    this.setState({
      step: step - 1,
    });
  };

  handleChange = (event: any) => {
    this.setState({ [event.target.name]: event.target.value });
  };

  render() {
    const { step, firstName, lastName, email, address, city, state, zip } =
      this.state;
    const inputValues = {
      firstName,
      lastName,
      email,
      address,
      city,
      state,
      zip,
    };
    switch (step) {
      case 1:
        return (
          <>
            <h3>{step}</h3>
            <UserDetails
              nextStep={this.nextStep}
              handleChange={this.handleChange}
              inputValues={inputValues}
            />
          </>
        );
      case 2:
        return (
          <>
            <h3>{step}</h3>
            <AddressDetails
              nextStep={this.nextStep}
              prevStep={this.prevStep}
              handleChange={this.handleChange}
              inputValues={inputValues}
            />
          </>
        );
      case 3:
        return (
          <>
            <h3>{step}</h3>
            <Confirmation
              nextStep={this.nextStep}
              prevStep={this.prevStep}
              inputValues={inputValues}
            />
          </>
        );
    }
  }
}

